/**
 * Функция, которая принимает на вход два массива чисел и возвращает новый массив, содержащий только элементы, которые есть в обоих массивах.
 * @param {number[]} arr1 - первый входной массив чисел
 * @param {number[]} arr2 - второй входной массив чисел
 * @return {number[]} - массив, содержащий только элементы, которые есть в обоих массивах
 */
function intersect(arr1, arr2) {
    // ваш код здесь
    const set1 = new Set(arr1);
       
    return arr2.filter(item => set1.has(item));
}

/*
new Set(arr1) — преобразуем первый массив в множество. Множество 
автоматически удаляет дублирующиеся значения и позволяет быстро проверять 
наличие элементов.

arr2.filter(item => set1.has(item)) — проходим по каждому элементу 
второго массива и с помощью метода filter() отбираем только те элементы, 
которые есть в первом множестве (set1.has(item) проверяет, содержится ли 
элемент в set1).

Функция возвращает массив общих элементов.
*/