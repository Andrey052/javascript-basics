/**
 * Функция, которая принимает на вход массив чисел и возвращает среднее значение всех элементов массива.
 * @param {number[]} arr - входной массив чисел
 * @return {number} - среднее значение всех элементов массива
 */
function average(arr) {
    // ваш код здесь
    if (arr.length === 0) {
        return 0;
    }
    let sum = 0;
    for (let i = 0; i < arr.length; i++) { 
        sum += arr[i];
    }
    let average = sum / arr.length;
        
    return average;
}

/*
Пояснение для начинающих:
if (arr.length === 0) — проверяем, пустой ли массив. Если он пустой, 
сразу возвращаем 0, потому что среднее значение не из чего вычислять.

let sum = 0 — создаем переменную sum, в которой будем хранить сумму всех 
чисел. Начинаем с нуля.

for (let i = 0; i < arr.length; i++) — с помощью цикла for проходим по 
каждому элементу массива. Мы начинаем с первого числа (под индексом 0) и 
идем до последнего числа в массиве.

sum += arr[i] — прибавляем текущее число к общей сумме. Каждый раз, когда
 цикл проходит новое число, оно добавляется к переменной sum.

let average = sum / arr.length — когда мы просуммировали все числа, делим 
сумму на количество элементов в массиве (arr.length) и сохраняем 
результат в переменную average.

return average — возвращаем результат, который получился.
*/